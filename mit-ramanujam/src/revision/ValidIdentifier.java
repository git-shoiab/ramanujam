package revision;

public class ValidIdentifier {
	public static void main(String[] args) {
		int weight;//valid name
		int _weight;//valid name - can start with _
		int $weight;//valid name - can start with $
		int we3ght;//valid name - can have numbers inbetween
		
		int we3gh_t$;//valid name - can be a combinationa lso
		
		//invalid names
		
		//int 2weight;//invalid name - cannot start with a number
		
		//int %weight;//other than _ and $, no other character is expected other than $ and _
		
		//int class;//invalid name - names cannot be key words 
		
		//Remember - Identifiers can be variable name, method name, class name, package name etc
		
	}
}
